        <h2 style="color:#00b0b6;" align="left" ;>Evolução dos Modelos Atômicos</h2>
        <h4 style="color:black;" align="left" ;>Sempre que era realizada uma nova descoberta, um novo modelo atômico era apresentado para que pudesse tentar esclarecer as mesmas.</h4>
        <h4 style="color:#008085;" align="left" ;>Leis Ponderais:</h4>
        <h4 style="color:black;" align="left" ;>LAVOISIER - Lei de Conservação da Massa</h4>
        <h4 style="color:black;" align="left" ;>PROUST - Lei de Proporções Fixas</h4>
        <h4 style="color:black;" align="left" ;>DALTON - Lei de Proporções Múltiplas</h4>
        <h4 style="color:#008085;" align="left" ;>Principais Modelos Atômicos:</h4>
        <h4 style="color:black;" align="left" ;>Modelo atômico de Dalton (1803) — esfera maciça, indivisível e indestrutível.</h4>
        <h4 style="color:black;" align="left" ;> Segundo Dalton: Todas as substâncias são formadas por átomos; Os átomos de um elemento químico são idênticos no tamanho e nas características, já os de elementos químicos distintos são diferentes; As substâncias são resultado de uma reação química,
            que consiste na recombinação dos átomos.</h4>
        <h4 style="color:black;" align="left" ;>Modelo atômico de Thomson (1898) - esfera de carga positiva com elétrons fixados.</h4>
        <h4 style="color:black;" align="left" ;> Segundo Thomson: O átomo é eletricamente neutro; Os elétrons fixam-se em uma superfície carregada positivamente; Existe uma repulsão entre os elétrons distribuídos nos átomos.</h4>
        <h4 style="color:black;" align="left" ;>Modelo atômico de Rutherford (1911) — esfera de carga positiva com elétrons fixados.</h4>
        <h4 style="color:black;" align="left" ;>Segundo Rutherford - O átomo apresenta uma região central com alta concentração de carga positiva; A massa de um átomo se concentra na sua região central; Os elétrons são mais leves e se localizam ao redor do núcleo, região que contém muitos espaços
            vazios.
        </h4>
        <h4 style="color:black;" align="left" ;>Modelo atômico de Bohr (1913) — os elétrons se movimentam em camadas circulares fixas ao redor do núcleo.</h4>
        <h4 style="color:black;" align="left" ;> Segundo Bohr - Os elétrons movimentam-se nas camadas ao redor do núcleo; As camadas ao redor do núcleo apresentam valores de energia específicos; Para ir para um nível mais externo o elétron deve absorver energia. Ao retornar para uma camada mais
            próxima do núcleo, o elétron libera energia.</h4>
        <h4 style="color:black;" align="left" ;>Modelo atômico quântico (1926) — o núcleo é formado por prótons (carga positiva) e nêutrons (carga nula), e os elétrons (carga negativa) formam uma nuvem eletrônica ao redor do núcleo.</h4>
        <h4 style="color:black;" align="left" ;> O núcleo é formado por prótons e nêutrons. Como apenas os prótons possuem carga, o núcleo é carregado positivamente; Os elétrons formam uma nuvem eletrônica ao redor do núcleo; Os elétrons se movimentam em orbitais, num espaço tridimensional;
            A posição exata de um elétron não pode ser definida. O que é feito são cálculos que determinam a probabilidade da região que um elétron estará em dado tempo</h4>
