        <h2 style="color:#00b0b6;" align="left" ;>Força</h2>
        <h4 style="color:#000000;" align="left" ;> O conceito da força explica a pressão exercida sobre tal objeto ou, as alterações da quantidade de movimento de um determinado corpo. A força (F) é um vetor, possuindo um módulo, direção e sentido.</h4>
        <h3 style="color:#008085;" align="left" ;>Classificações:</h3>
        <h4 style="color:black;" align="left" ;> Forças de contato - Agem sobre os corpos somente na medida que quem aplica a força está necessariamente em contato com os corpos.</h4>
        <h4 style="color:black;" align="left" ;> Forças de campo - Agem sobre os corpos sendo que o corpo que exerce a força não se encontra em contato os outros.</h4>
        <h3 style="color:#008085;" align="left" ;>Tipos de Força:</h3>
        <h4 style="color:black;" align="left" ;>Força Peso - Força que existe sobre todos os corpos, sendo exercida sobre eles por meio do campo gravitacional.</h4>
        <h4 style="color:black;" align="left" ;>Força Elástica - Força exercida sobre uma mola, "deformando-a".</h4>
        <h4 style="color:black;" align="left" ;>Força Centrípeta - Força que exerce movimento circular, com determinada aceleração.</h4>
        <h4 style="color:black;" align="left" ;>Força Magnética - Força de atração e repulsão.</h4>
        <h4 style="color:black;" align="left" ;>Força Gravitacional - Força de atração mútua entre os corpos físicos.</h4>
        <h4 style="color:black;" align="left" ;>Força de Atrito - Força exercida entre duas superfícies que estão em contato.</h4>
        <h4 style="color:black;" align="left" ;>Força Normal - Força exercida por um corpo sob uma superfície.</h4>
