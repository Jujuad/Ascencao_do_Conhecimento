        <h2 style="color:#00b0b6;" align="left" ;>Primeira Guerra Mundial</h2>
        <h4 style="color:black;" align="left" ;> A Primeira Guerra Mundial (28 de julho de 1914 -11 de novembro de 1918). Foi consequência de inúmeros fatores, como a rivalidade econômica, ressentimentos por acontecimentos passados e questões nacionalistas. </h4>
        <h3 style="color:#008085;" align="left" ;>As principais causas foram: </h3>
        <h4 style="color:black;" align="left" ;> Paz armadas - Onde os países europeus começaram a investir em diversos armamentos. </h4>
        <h4 style="color:black;" align="left" ;> Corrida Armamentista – Onde a rivalidade entre as nações imperialistas iniciou essa corrida. Também foi quando o império alemão se transformou em potência naval. </h4>
        <h4 style="color:black;" align="left" ;> Além do nacionalismo e imperialismo. </h4>
        <h3 style="color:#008085;" align="left" ;>Alianças: </h3>
        <h4 style="color:black;" align="left" ;> Os países envoltos na guerra se dividiram em dois grupos, de acordo com suas rivalidades e o sistema de aliança. </h4>
        <h4 style="color:black;" align="left" ;> Tríplice Entente: Rússia, França e Inglaterra. </h4>
        <h4 style="color:black;" align="left" ;> Tríplice Aliança: Alemanha e Áustria-Hungria, Império Turco-Otomano e Bulgária. </h4>
        <h3 style="color:#008085;" align="left" ;>O decorrer da Guerra</h3>
        <h4 style="color:black;" align="left" ;> Teve duas fases: Guerra de Movimento e Guerra de Trincheira. A guerra de Trincheira é caracterizada por ter tido um alto grau de mortalidade dos soldados envolvidos. </h4>
        <h4 style="color:black;" align="left" ;> Seu resultado foi um trauma drástico, uma geração traumatiza pelos horrores da guerra. Os Estados Unidos (que só se envolveram na guerra por cerca de 1917) não sofreram tantos impactos neste conflito, pois seu território estava intacto e sua indústria
            funcionava perfeitamente. </h4>
        <h3 style="color:#008085;" align="left" ;>O fim da primeira guerra</h3>
        <h4 style="color:black;" align="left" ;> Com a chegada dos Estados Unidos na guerra e a perca dos recursos, a Alemanha não tinha mais condição de seguir lutando, com o decorrer dos acontecimentos a Alemanha passa a ser uma república parlamentar. As nações beligerantes se reuniram na
            França, afim de chegar a um consenso para as condições da paz. A Alemanha acabou por ser considerada culpada da guerra e teve de ceder territórios para as nações fronteiriças. Perdendo suas colônias africanas, a Alemanha teve que consentir
            com a independência da Áustria.</h4>
        <h4 style="color:black;" align="left" ;> A guerra totalizou cerca de 23 milhões de mortos. </h4>
