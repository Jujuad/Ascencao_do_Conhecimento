        <h2 style="color:#00b0b6;" align="left" ;>Colonização do Brasil</h2>
        <h4 style="color:black;" align="left" ;> A colonização portuguesa no Brasil teve como principais características: civilizar, exterminar, explorar, povoar, conquistar e dominar. Isso ocorreu por volta do ano de 1500 (mesmo que so tenha sido efetivada por volta de 1530), quando os portugueses
            chegaram no denominado “mundo novo”, como consequência das navegações de exploração oceânica realizadas por Portugal, que acabaram por submeter ao seu domínio os indígenas. </h4>
        <h4 style="color:black;" align="left" ;> Ela contou com três ciclos econômicos: pau-brasil, açúcar e ouro. Utilizavam da mão de obra de escravizados indígenas e africanos, que foram obtidos por bandeirantes ou pelo tráfico negreiro. </h4>
