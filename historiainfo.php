    <div class="cfinformações">

        <h2 style="color:#00b0b6;" align="left" ;>Povos da Mesopotâmia</h2>
        <h4 style="color:black;" align="left" ;> Localizada entre os rios Tigre e Eufrates (fazendo jus a seu nome), a Mesopotâmia é considerada um dos berços da civilização. Diversos povos habitaram a Mesopotâmia, seus solos eram extremamente férteis e atraiam muitas pessoas. </h4>
        <h2 style="color:#008085;" align="left" ;>Os principais povos que habitaram a Mesopotâmia:</h2>
        <h3 style="color:#008085;" align="left" ;>Sumérios </h3>
        <h4 style="color:black;" align="left" ;> Foram os primeiros povos da Mesopotâmia, fundando as primeiras cidades da região, que logo se tornaram cidades-estado.</h4>
        <h4 style="color:black;" align="left" ;> Também foram os próprios que criaram a escrita cuneiforme, a primeira da humanidade. Desenvolveram-na para controlar a contabilidade do comércio, da produção agrícola e do palácio real, por volta de 3000 a.C. </h4>
        <h3 style="color:#008085;" align="left" ;>Acádios </h3>
        <h4 style="color:black;" align="left" ;> Por volta de 2200 a.C., o Império Acádio foi fundado por um povo que habitou a região, conquistando as cidades da Suméria. Esse Império foi um dos primeiros centralizados da humanidade. </h4>
        <h4 style="color:black;" align="left" ;> O Império não durou por muito tempo, ocorreram muitas rebeliões, ligadas a chegada de povos estrangeiros na região. Com os motins internos e o enfraquecimento do império, os amoritas conquistaram o poder.</h4>
        <h3 style="color:#008085;" align="left" ;>Amoritas </h3>
        <h4 style="color:black;" align="left" ;> Os amoritas eram um povo semita, abrigados a região de Babilônia. Seu Império é conhecido como o Primeiro de Babilônia e, a cidade se tornou um importante centro comercial da Mesopotâmia.</h4>
        <h4 style="color:black;" align="left" ;> Os Amoritas tinham Hamurábi como seu rei, ele foi responsável pela expansão das fronteiras do Império Babilônico, além de plantios agrícolas e canais de irrigação. Seu nome também é conhecido pelo conjunto de leis que criou, o “Código de Hamurábi”.
            Todas voltadas para lei de Talião “olho por olho, dente por dente”, então, qualquer malfeitoria realizada, teria uma sanção proporcional ao ato.</h4>
        <h4 style="color:black;" align="left" ;> Depois da morte de seu rei, os Amoritas tiveram um grande enfraquecimento e sofreram algumas indisciplinas e ocorreram alguns ataques de povos estrangeiros, tais como os cassitas e os hititas.</h4>
        <h3 style="color:#008085;" align="left" ;>Assírios </h3>
        <h4 style="color:black;" align="left" ;> Os Assírios habitaram o norte da Mesopotâmia, e organizaram uma sociedade militarizada, que foi considerada a mais poderosa e organizada de sua era. Tem sua fama por guerreiros cruéis, e fizeram a conquista da Mesopotâmia, chegando a expugnar
            quase toda a região.</h4>
        <h4 style="color:black;" align="left" ;> Seu reinado mais conhecido foi o de Assurbanipal, que construiu a Biblioteca de Nínive. Durante seu domínio, o império sofreu um declive e foi conquistado pelos Caldeus.</h4>
        <h3 style="color:#008085;" align="left" ;>Caldeus </h3>
        <h4 style="color:black;" align="left" ;> Compuseram o Segundo Império Babilônico, seu império foi extremamente curto, contando com os comando de Nabucodonosor, que ordenou a destruição de Jerusalém e algumas construções grandes em Babilônia. </h4>
        <h4 style="color:black;" align="left" ;> Após a morte de seu rei, os Caldeus perderam seu domínio na região e foram dominados pelos persas, que estavam sob comando de Ciro II. </h4>
