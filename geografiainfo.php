    <h2 style="color:#00b0b6;" align="left" ;>Circulação Atmosférica</h2>
    <h4 style="color:black;" align="left" ;> A circulação atmosférica acontece conjuntamente do desequilíbrio da radiação recebida pela Terra ao longo de sua extensão. </h4>
    <h4 style="color:black;" align="left" ;> As regiões que se localizam mais próximas da Linha do Equador recebem mais radiação solar e, são mais aquecidas. Regiões polares, tem o índice de radiação menor e o aquecimento também. Então, para seja equilibrado, acontecem muitas trocas de massas
        de ar entre as regiões mais quentes e as mais frias, que acontecem a partir das diferenças de pressão. </h4>
    <h3 style="color:#008085;" align="left" ;>A circulação atmosférica tem três células de movimentação. </h3>
    <h4 style="color:black;" align="left" ;> Célula Tropical – Ocorre nas regiões de baixa latitude, caracterizada pelo movimento em direção a porção superior da atmosfera. </h4>
    <h4 style="color:black;" align="left" ;> Célula de Ferrel – Ocorre nas latitudes intermediárias, caracteriza-se pela direção do movimento em direção a porção inferior da atmosfera. </h4>
    <h4 style="color:black;" align="left" ;> Célula Polar – O ar das outras células ao alcançar seus polos, descem e formam uma grande pressão. E assim, a superfície desloca o ar para as zonas de menor pressão e tropicais. </h4>
