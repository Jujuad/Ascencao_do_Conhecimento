        <h2 style="color:#00b0b6;" align="left" ;>Climas no Mundo</h2>
        <h4 style="color:black;" align="left" ;> Os climas no mundo são extremamente variados. Tendo variantes a partir das variadas massas de ar, localização geográfica, entre outros. </h4>
        <h3 style="color:#008085;" align="left" ;>Alguns tipos de Climas são:</h3>
        <h4 style="color:black;" align="left" ;> Equatorial: possui temperaturas médias acima de 25°C; clima quente e úmido, com índices pluviométricos anuais acima de 2000 mm. </h4>
        <h4 style="color:black;" align="left" ;> Tropical: temperatura variando em 20°C no inverno e 25°C no verão, com duas estações bem definidas, uma seca e outra chuvosa. </h4>
        <h4 style="color:black;" align="left" ;> Subtropical: possui temperaturas médias entre 15°C e 20°C no verão, e no inverno as médias variam entre 0°C a 10°C, chuvas bem distribuídas. </h4>
        <h4 style="color:black;" align="left" ;> Oceânico: recebe influência dos oceanos e mares, tornando os invernos menos rigorosos. </h4>
        <h4 style="color:black;" align="left" ;> Continental: praticamente não recebe influência dos oceanos, possui um inverno mais rigoroso. </h4>
        <h4 style="color:black;" align="left" ;> Mediterrâneo: possui invernos chuvosos e verões quentes, com quatro estações bem definidas. </h4>
        <h4 style="color:black;" align="left" ;>Desértico: as temperaturas médias anuais variam entre 20°C e 30°C, os índices de precipitações não ultrapassam 250 mm ao ano. </h4>
        <h4 style="color:black;" align="left" ;> Semiárido: caracterizado por altas temperaturas podendo chegar a 32°C, os índices pluviométricos são inferiores a 600 mm anuais e as chuvas são irregulares. </h4>
        <h4 style="color:black;" align="left" ;> Frio (subpolar): possui índices pluviométricos anuais variando, dependendo da região, entre 200 mm e 1000 mm, características de um inverno negativo e verão com temperaturas por volta de 10°C. </h4>
        <h4 style="color:black;" align="left" ;> Frio de montanha: temperatura determinada pela altitude, quanto mais alto mais frio, mesmo em regiões tropicais. </h4>
        <h4 style="color:black;" align="left" ;> Polar: caracteriza-se por longos invernos e verões secos e curtos, temperaturas anuais sempre abaixo de zero, e marcante presença de neve e gelo. </h4>
